        private TileMap tileMap;
        private Player player;

        private Camera kamera;

        private Dictionary<string, Texture2D> karakterSpriteSet;
        private Texture2D cimen, tas;

        public Game1()
        {
            _graphics = new GraphicsDeviceManager(this);
            Content.RootDirectory = "Content";
            IsMouseVisible = true;
        }

        protected override void Initialize()
        {
            _graphics.PreferredBackBufferWidth = 800;
            _graphics.PreferredBackBufferHeight = 600;
            _graphics.ApplyChanges();

            kamera = new Camera(_graphics.PreferredBackBufferWidth, _graphics.PreferredBackBufferHeight);
            base.Initialize();
        }

        protected override void LoadContent()
        {
            _spriteBatch = new SpriteBatch(GraphicsDevice);

            cimen = Content.Load<Texture2D>("cimen");
            tas = Content.Load<Texture2D>("tass"); // ✅ Yeni 32x32 taş görseli

            tileMap = new TileMap(harita, cimen, tas);

            karakterSpriteSet = new Dictionary<string, Texture2D>()
            {
                { "down", Content.Load<Texture2D>("WalkingFront0") },
                { "up", Content.Load<Texture2D>("WalkingFront0") },
                { "left", Content.Load<Texture2D>("WalkingLeft0") },
                { "right", Content.Load<Texture2D>("WalkingRight0") }
            };

            player = new Player(karakterSpriteSet, new Vector2(200, 100), tileMap);
        }

        protected override void Update(GameTime gameTime)
        {
            if (Keyboard.GetState().IsKeyDown(Keys.Escape))
                Exit();

            player.Guncelle(gameTime);
            kamera.TakipEt(player.Pozisyon);

            base.Update(gameTime);
        }

        protected override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.CornflowerBlue);

            _spriteBatch.Begin(transformMatrix: kamera.GetMatrix());
            tileMap.Ciz(_spriteBatch);
            player.Ciz(_spriteBatch);
            _spriteBatch.End();

            base.Draw(gameTime);
        }
